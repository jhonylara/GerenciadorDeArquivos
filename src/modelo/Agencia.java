package modelo;

public class Agencia {
	
	private int agenciaID;
	private String agenciaName;
	private int regional_regionalID;
	
	public int getRegional_regionalID() {
		return regional_regionalID;
	}
	public void setRegional_regionalID(int regional_regionalID) {
		this.regional_regionalID = regional_regionalID;
	}
	public String getAgenciaName() {
		return agenciaName;
	}
	public void setAgenciaName(String agenciaName) {
		this.agenciaName = agenciaName;
	}
	public int getAgenciaID() {
		return agenciaID;
	}
	public void setAgenciaID(int agenciaID) {
		this.agenciaID = agenciaID;
	}
	
	

}
